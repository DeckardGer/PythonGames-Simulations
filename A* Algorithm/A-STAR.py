def main():
    pygame.init()
    screen = pygame.display.set_mode((SCREEN_SIZE_X, SCREEN_SIZE_Y))
    pygame.display.set_caption("A* Algorithm")
    font = pygame.font.Font('freesansbold.ttf', 16)
    
    #a_star_algorithm((0, 4), (6, 1))
    
    #randomise()
    GRID[0][0] = 0
    GRID[GRID_SIZE_Y - 1][GRID_SIZE_X - 1] = 0
    start = (5, 5) # 0, 5
    goal = (20, 20) # 10, 2
    
    openSet = []
    closedSet = []
    route = []
    
    distX = abs(goal[0] - start[0])
    distY = abs(goal[1] - start[1])
    if distX > distY:
        hScore = 14 * distY + 10 * (distX - distY)
    else:
        hScore = 14 * distX + 10 * (distY - distX)
        
    openSet.append(Node(start, 0, hScore, hScore, None))
    
    timer = 0
    set = 0
    complete = False
    
    running = True
    while running:
        screen.fill(WHITE)
        
        for event in pygame.event.get():
            if event.type == pygame.QUIT:
                running = False
                
            if event.type == pygame.KEYDOWN:
                if event.key == pygame.K_SPACE:
                    if len(openSet) > 0:
                        print()
                        nextStep(start, goal, openSet, closedSet)
            elif event.type == pygame.MOUSEBUTTONDOWN:
                y, x = pygame.mouse.get_pos()
                if GRID[int(x / TILE_SIZE)][int(y / TILE_SIZE)] == 1:
                    GRID[int(x / TILE_SIZE)][int(y / TILE_SIZE)] = 0
                else:
                    GRID[int(x / TILE_SIZE)][int(y / TILE_SIZE)] = 1
               
        if set > timer and not complete and len(openSet) > 0:
            route = nextStep(start, goal, openSet, closedSet)
            if route != None:
                complete = True
            set = 0
        set += 1
        
        showGrid(screen, font, openSet, closedSet, start, goal, route)
        
        pygame.display.flip()
    pygame.quit()

if __name__ == "__main__":
    main()